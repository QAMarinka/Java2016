public class _01DataTypes {
    public static void main(String[] args){
//  <data type> <variable name> = <data>;
        int age = 15;
        System.out.println(age);

        byte b = 15;

        long l = 5888888888L;

        short s = 13;

        float f = 25.7f;

        double d = 37.8;

        char letter = 'f';

        System.out.println(letter);

        boolean bool = true;
        boolean bool2 = false;

        System.out.println(bool);
    }
}
